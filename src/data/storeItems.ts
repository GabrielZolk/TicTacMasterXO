// ==================== HELPERS ====================

export const getItemById = (itemId: string): StoreItem | undefined => {
    return ALL_STORE_ITEMS.find(item => item.id === itemId);
};

export const getItemsByType = (type: StoreItem['type']): StoreItem[] => {
    return ALL_STORE_ITEMS.filter(item => item.type === type);
};

export const getItemsByRarity = (rarity: StoreItem['rarity']): StoreItem[] => {
    return ALL_STORE_ITEMS.filter(item => item.rarity === rarity);
};
